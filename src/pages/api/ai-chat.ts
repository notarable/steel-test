export async function POST({ request }: { request: Request }) {
  try {
    const { message, history } = await request.json();

    if (!message || typeof message !== "string" || message.trim().length === 0) {
      return new Response(
        JSON.stringify({ reply: "Пожалуйста, задайте вопрос." }),
        { status: 400, headers: { "Content-Type": "application/json" } }
      );
    }

    const apiKey = import.meta.env.OPENAI_API_KEY;
    if (!apiKey || apiKey.startsWith("sk-ВСТАВЬ")) {
      return new Response(
        JSON.stringify({ reply: getFallbackReply(message) }),
        { headers: { "Content-Type": "application/json" } }
      );
    }

    const openai = new OpenAI({ apiKey });

    const messages: OpenAI.ChatCompletionMessageParam[] = [
      { role: "system", content: SYSTEM_PROMPT },
    ];

    if (Array.isArray(history)) {
      for (const h of history.slice(-10)) {
        if (h.role === "user" || h.role === "assistant") {
          messages.push({ role: h.role, content: String(h.content).slice(0, 500) });
        }
      }
    }

    messages.push({ role: "user", content: message.slice(0, 1000) });

    const completion = await openai.chat.completions.create({
      model: "gpt-4o-mini",
      messages,
      max_tokens: 400,
      temperature: 0.7,
    });

    const reply = completion.choices[0]?.message?.content || "Извините, не удалось сформировать ответ.";

    return new Response(
      JSON.stringify({ reply }),
      { headers: { "Content-Type": "application/json" } }
    );
  } catch (err: any) {
    console.error("[ai-chat] Error:", err?.message || err);

    if (err?.status === 429) {
      return new Response(
        JSON.stringify({ reply: "Слишком много запросов. Подождите немного и попробуйте снова." }),
        { status: 429, headers: { "Content-Type": "application/json" } }
      );
    }

    return new Response(
      JSON.stringify({ reply: "Произошла ошибка. Попробуйте позже или свяжитесь с нами по телефону." }),
      { status: 500, headers: { "Content-Type": "application/json" } }
    );
  }
}

